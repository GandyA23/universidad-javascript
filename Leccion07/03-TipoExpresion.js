// Las funciones de tipo expresión (o funciones anonimas) pueden ser asignadas a variables y no requerirán de un nombre.​​

// Definción de una función anonima
let sumar = function (a, b) {
    return a+b
};

// Llamar a la función anonima
console.log(sumar(1, 2));
console.log(sumar(3, 4));
console.log(sumar(5, 6));
