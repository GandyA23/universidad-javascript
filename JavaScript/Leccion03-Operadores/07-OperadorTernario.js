// Estructura básica del operador ternario
let resultado1 = 3 > 2 ? "VERDADERO" : "FALSO";
let resultado2 = 3 < 2 ? "VERDADERO" : "FALSO";

console.log(resultado1);
console.log(resultado2);

// Número par con operador ternario
let num1 = 10 % 2 ? "IMPAR" : "PAR";
let num2 = 9 % 2 ? "IMPAR" : "PAR";

console.log(num1);
console.log(num2);
