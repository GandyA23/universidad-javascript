// Todos los ciclos ejecutan su bloque de código siempre y cuando las condición sea verdadera

// while - mientras

let contador = 0;

// Mientras el contador sea menor que 3
// 1, 2, 3
while (contador++ < 3)
    console.log(contador);

contador = 0;

// 0, 1, 2
while (contador < 3) {
    console.log(contador);
    contador++;
}
