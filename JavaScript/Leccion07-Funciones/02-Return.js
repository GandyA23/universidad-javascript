// Declaración de una función con un valor de retorno
function miFuncion(a, b) {
    // Palabra reservada return 
    // Es posible retornar cualquier tipo de dato
    return a + b;
}

// Cuando la función es llamada, regresara la suma de los dos números
console.log(miFuncion(1, 2));
console.log(miFuncion(3, 4));
console.log(miFuncion(5, 6));
