// Siempre es lo último a evaluar

// Asignación normal
let a = 1;

// Operaciones de asignación

// Asignación compuesta
a += 3; // a = a + 3;
console.log(a);

a -= 2; // a = a - 2; 
console.log(a);

a *= 2; // a = a * 2; 
console.log(a);

a /= 2; // a = a / 2; 
console.log(a);

// Exponente
a **= 2; // a = a ** 2; 
console.log(a);
